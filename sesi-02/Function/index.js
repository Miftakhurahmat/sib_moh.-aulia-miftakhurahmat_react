//function declaration
function add(a, b) {
	return a + b;
}

//function expression
const add = function (a, b) {
	return a + b;
}

//arrow function
const add = (a, b) => {
	return a + b;
}

function square(x) {
	return x * x;
}

function three() {
	return 3;
}

const add = (a, b) => a + b
const square = x => x * x
const three = () => 3

const add = (a, b) => {
	const temp = a + b;
	return temp;
}
